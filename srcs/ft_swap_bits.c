#include <inttypes.h>

void	ft_swap_bits(uint32_t *ptr, uint32_t idx1, uint32_t idx2)
{
	idx1 %= 32;
	idx2 %= 32;
	if ((*ptr >> idx1 & 1U) != (*ptr >> idx2 & 1U))
	{
		*ptr ^= 1U << idx1;
		*ptr ^= 1U << idx2;
	}
}
