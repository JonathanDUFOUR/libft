#include <stdint.h>
#include <unistd.h>

void	ft_putnbr_bin(uint32_t n)
{
	char		d;

	if (n > 1)
		ft_putnbr_bin(n / 2);
	d = n % 2 + '0';
	write(1, &d, 1);
}
