#include <stdlib.h>
#include <sys/types.h>
#include "libft.h"

char	*ft_strndup(char const *s, size_t n)
{
	char	*output;

	output = ft_calloc((n + 1), sizeof(char));
	if (output)
		ft_memcpy(output, s, n);
	return (output);
}
