#include <inttypes.h>
#include <unistd.h>

void	ft_putbyte(uint8_t byte)
{
	int	i;

	i = 8;
	while (--i >= 0)
	{
		if ((byte >> i) & 1)
			write(1, "1", 1);
		else
			write(1, "0", 1);
	}
}
