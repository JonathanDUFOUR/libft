#include "libft.h"

void	ft_print_bits(unsigned nbr, size_t n)
{
	while (n--)
	{
		ft_padded_putnbr_base((nbr >> 8 * n) & 0xFF, "01", 8);
		if (n)
			ft_putchar(' ');
	}
}
