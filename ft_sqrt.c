int	ft_sqrt(int nbr)
{
	int	sqrt;

	sqrt = 0;
	while (sqrt <= nbr / 2 && sqrt * sqrt != nbr)
		sqrt++;
	return (sqrt * sqrt == nbr ? sqrt : 0);
}
