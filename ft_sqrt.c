#include <sys/types.h>
#include <stdint.h>

float	ft_sqrt(uint32_t n)
{
	float	a;
	int		i;

	a = n / 2;
	i = -1;
	while (++i < 100)
		a = (a + (n / a)) / 2;
	return (a);
}
