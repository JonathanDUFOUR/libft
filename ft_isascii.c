/*
**	What does a ternary
**
**	(c ? a : b)
**		<=>
**	((c * a) + (!c * b))
*/

/*
**	Methodes possibles
**
**	1/	return (c >= 0 && c <= 127);
**	2/	return (c >> 7)
*/

int	ft_isascii(int c)
{
	return (c >= 0 && c <= 127);
}
