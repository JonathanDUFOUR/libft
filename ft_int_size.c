int	ft_int_size(int nbr)
{
	unsigned int	abs;
	int				sign;
	int				size;

	sign = (nbr < 0 ? 1 : 0);
	abs = (sign ? -nbr : nbr);
	size = (sign ? 2 : 1);
	while (abs > 9)
	{
		size++;
		abs /= 10;
	}
	return (size);
}
