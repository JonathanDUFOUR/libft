int	ft_do_op(int a, int b, char c, char *op)
{
	if (c == op[0])
		return (a + b);
	if (c == op[1])
		return (a - b);
	if (c == op[2])
		return (a * b);
	if (c == op[3])
		return (a / b);
	if (c == op[4])
		return (a % b);
	return (0);
}
